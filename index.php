    $('.btn-submit').click(() => {
        let form_errors = false;
        let name = $('input[name="name"]').val();
        let email = $('input[name="email"]').val();
        let subject = $('input[name="subject"]').val();
        let message = $('textarea[name="message"]').val();

        if (name.length < 2 || name.length > 60) {
            form_errors = true;
            setError('name', 'input');
        } else {
            cleanError('name', 'input');
        }


        if (subject.length < 2 || subject.length > 60) {
            form_errors = true;
            setError('subject', 'input');
        } else {
            cleanError('subject', 'input');
        }


        if (message.length < 2 || message.length > 255) {
            form_errors = true;
            setError('message', 'textarea');
        } else {
            cleanError('message', 'textarea');
        }


        if (!isEmail(email)) {
            form_errors = true;
            setError('email', 'input');
        } else {
            cleanError('email', 'input');
        }

        if (!form_errors) {
            $('#contactForm').submit();
        }
    });

    function setError(name, type) {
        let input = $(type + '[name="' + name + '"]');
        input.css('border', '1px solid red');
    }

    function cleanError(name, type) {
        let input = $(type + '[name="' + name + '"]');
        input.css('border', 'none');
    }

    function isEmail(email) {
        const re = /^(([^<>()\[\]\\.,;:\s@"]+(\.[^<>()\[\]\\.,;:\s@"]+)*)|(".+"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/;
        return re.test(String(email).toLowerCase());
    }

